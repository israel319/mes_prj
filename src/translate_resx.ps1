# Load XML
$xml = [xml](Get-Content $file -Raw -Encoding UTF8)
$count = 0

foreach ($node in $xml.root.data) {
    $key = $node.name
    if ($translations.ContainsKey($key)) {
        $newVal = $translations[$key]
        $oldVal = $node.value
        if ($oldVal -ne $newVal) {
            $node.value = $newVal
            $count++
            Write-Host "  $key : '$oldVal' -> '$newVal'"
        }
    }
}

Write-Host ""
Write-Host "Total replaced: $count"

# Save with UTF-8 encoding
$xml.Save($file)
Write-Host "File saved."
